n = float(input("Enter the first number: "))
o = str(input("Enter the operator (+ or - or / or *) : "))
m = float(input("Enter the second number: "))

if o == "+":
    print("Addition: ",n+m)

if o == "-":
    print("Substraction: ",n-m)

if o == "/":
    print("Division: ",n/m)
    
if o == "*":
    print("Multiplication: ",n*m)



